#Casting
#Casting Adalah mengubah tipedata ke tipe data lainny
# tipe data , Int, float , string , boolean
print("====INTEGER====")

nomor = 40 # INT : Merubah tipe data int ke tipe data lainnya 
print ("nomor", nomor, ",type", type(nomor))

data_float = float(nomor)
print ("nomor", data_float, ",type", type(data_float))

data_String = str(nomor)
print ("nomor", data_String, ",type", type(data_String))

data_boolean = bool(nomor)
print ("nomor", data_boolean, ",type", type(data_boolean))

print("====FLOAT ====")
#Float : Merubah Tipe data float ke tipe data lainnya
